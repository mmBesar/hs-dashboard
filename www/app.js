function getIcon(key) { return ICONS[key] || ICON_FALLBACK; }

// ── Theme ─────────────────────────────────────────────────────────────────────
function applyTheme(theme) {
  const root = document.documentElement;
  const resolved = theme === 'auto'
    ? (window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light')
    : theme;
  root.setAttribute('data-theme', resolved);
  document.querySelectorAll('.theme-btn').forEach(b => b.classList.toggle('active', b.dataset.theme === theme));
  localStorage.setItem('hs-theme', theme);
}
function setTheme(t) { applyTheme(t); }
function initTheme(def) { applyTheme(localStorage.getItem('hs-theme') || def || 'auto'); }
window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', () => {
  if ((localStorage.getItem('hs-theme') || 'auto') === 'auto') applyTheme('auto');
});

// ── Clock ─────────────────────────────────────────────────────────────────────
function startClock(tz) {
  function tick() {
    const now = new Date();
    document.getElementById('clock').textContent = now.toLocaleTimeString('en-GB', {timeZone:tz,hour:'2-digit',minute:'2-digit',second:'2-digit',hour12:false});
    document.getElementById('date').textContent  = now.toLocaleDateString('en-GB',  {timeZone:tz,weekday:'short',day:'numeric',month:'short',year:'numeric'});
  }
  tick(); setInterval(tick, 1000);
}

// ── Fetch ─────────────────────────────────────────────────────────────────────
async function fetchJSON(url) {
  try {
    const r = await fetch(url + '?_=' + Date.now());
    if (!r.ok) throw new Error(r.status);
    return await r.json();
  } catch { return null; }
}

// ── Helpers ───────────────────────────────────────────────────────────────────
function formatUptime(s) {
  const d=Math.floor(s/86400), h=Math.floor((s%86400)/3600), m=Math.floor((s%3600)/60);
  if(d>0) return `${d}d ${h}h`; if(h>0) return `${h}h ${m}m`; return `${m}m`;
}
function barClass(p) { return p>=90?'danger':p>=70?'warn':''; }
function tempColor(c) {
  if(c>=80) return 'var(--ctp-red)'; if(c>=65) return 'var(--ctp-peach)'; return 'var(--ctp-green)';
}

// ── Stats ─────────────────────────────────────────────────────────────────────
let _arch = null;

function updateStats(data) {
  if (!data || !data.timestamp) return;

  // CPU
  const cpu = Math.round(data.cpu_percent);
  document.getElementById('stat-cpu').textContent = cpu + '%';
  const bc = document.getElementById('bar-cpu');
  bc.style.width = cpu + '%'; bc.className = 'stat-bar-fill ' + barClass(cpu);

  // RAM
  const ram = Math.round(data.ram_percent);
  document.getElementById('stat-ram').textContent = ram + '%';
  document.getElementById('stat-ram-sub').textContent = Math.round(data.ram_used_mb) + ' / ' + Math.round(data.ram_total_mb) + ' MB';
  const br = document.getElementById('bar-ram');
  br.style.width = ram + '%'; br.className = 'stat-bar-fill ' + barClass(ram);

  // Load
  document.getElementById('stat-load').textContent = data.load_1m.toFixed(2);
  document.getElementById('stat-load-sub').textContent = data.load_1m.toFixed(2)+' · '+data.load_5m.toFixed(2)+' · '+data.load_15m.toFixed(2);

  // Uptime
  document.getElementById('stat-uptime').textContent = formatUptime(data.uptime_seconds);

  // Temps — dynamic
  const tc = document.getElementById('temps-container');
  if (tc && data.temps && data.temps.length > 0) {
    tc.innerHTML = '';
    data.temps.forEach(t => {
      tc.innerHTML += `
        <div class="stat-card">
          <div class="stat-label">${t.label}</div>
          <div class="stat-value" style="color:${tempColor(t.value)}">${t.value}°C</div>
          <div class="stat-sub">${t.description||''}</div>
        </div>`;
    });
  }

  // GPUs — dynamic
  const gc = document.getElementById('gpus-container');
  if (gc && data.gpus && data.gpus.length > 0) {
    gc.innerHTML = '';
    data.gpus.forEach(g => {
      const vram = g.vram_total_mb > 0
        ? `<div class="stat-sub">${g.vram_used_mb} / ${g.vram_total_mb} MB VRAM</div>` : '';
      gc.innerHTML += `
        <div class="stat-card">
          <div class="stat-label">${g.vendor} GPU ${g.label}</div>
          <div class="stat-value" style="color:${tempColor(g.temp)}">${g.temp > 0 ? g.temp+'°C' : '—'}</div>
          ${g.usage_percent > 0 ? `<div class="stat-sub">${g.usage_percent.toFixed(1)}% usage</div>` : ''}
          ${vram}
          <div class="stat-bar"><div class="stat-bar-fill ${barClass(g.usage_percent)}" style="width:${g.usage_percent}%"></div></div>
        </div>`;
    });
  }

  // Disks — dynamic
  const dc2 = document.getElementById('disks-container');
  if (dc2 && data.disks && data.disks.length > 0) {
    dc2.innerHTML = '';
    data.disks.forEach(d => {
      dc2.innerHTML += `
        <div class="stat-card">
          <div class="stat-label">Disk ${d.mount}</div>
          <div class="stat-value">${d.percent}%</div>
          <div class="stat-sub">${d.used_gb} / ${d.total_gb} GB</div>
          <div class="stat-bar"><div class="stat-bar-fill ${barClass(d.percent)}" style="width:${d.percent}%"></div></div>
        </div>`;
    });
  }

  // Arch
  if (data.arch && data.arch.trim()) {
    _arch = data.arch.trim();
    const badge = document.getElementById('arch-tag');
    if (badge) { badge.textContent = _arch; badge.style.display = 'inline-block'; }
    const av = document.getElementById('info-arch-value');
    if (av) av.innerHTML = `<span class="tag" style="background:rgba(198,160,246,0.15);color:var(--accent);border:1px solid rgba(198,160,246,0.3)">${_arch}</span>`;
  }

  document.getElementById('footer-updated').textContent = 'updated ' + new Date(data.timestamp*1000).toLocaleTimeString('en-GB');
}

// ── Status ────────────────────────────────────────────────────────────────────
let statusData = {};
async function refreshStatus() {
  const data = await fetchJSON('/api/status');
  if (data && data.services) {
    statusData = data.services;
    Object.entries(statusData).forEach(([url, state]) => {
      document.querySelectorAll(`[data-status-url="${CSS.escape(url)}"]`).forEach(el => {
        el.className = 'status-badge ' + state; el.textContent = state;
      });
    });
  }
}

// ── Services ──────────────────────────────────────────────────────────────────
function renderServices(servers) {
  const container = document.getElementById('services-container');
  container.innerHTML = '';
  servers.forEach(server => {
    const label = document.createElement('div');
    label.className = 'section-label';
    label.textContent = server.label ? `services · ${server.name} · ${server.label}` : `services · ${server.name}`;
    container.appendChild(label);
    const grid = document.createElement('div');
    grid.className = 'services-grid';
    server.services.forEach(svc => {
      const disabled = !!svc.disabled;
      const accent = svc.accent || 'var(--accent)';
      const iconBg = accent.startsWith('#') ? accent + '20' : 'rgba(198,160,246,0.15)';
      const state = disabled ? 'soon' : (statusData[svc.url] || 'checking');
      const card = document.createElement('a');
      card.href = disabled ? '#' : svc.url;
      if (!disabled) card.target = '_blank';
      card.rel = 'noopener noreferrer';
      card.className = 'service-card' + (disabled ? ' disabled' : '');
      card.style.setProperty('--card-accent', accent);
      card.style.setProperty('--icon-bg', iconBg);
      card.innerHTML = `
        <div class="card-top">
          <div class="card-icon" style="color:${accent}">${getIcon(svc.icon)}</div>
          <span class="status-badge ${state}" data-status-url="${svc.url}">${state}</span>
        </div>
        <div class="card-name">${svc.name}</div>
        <div class="card-desc">${svc.desc}</div>
        <div class="card-url">${svc.display||svc.url}</div>`;
      grid.appendChild(card);
    });
    container.appendChild(grid);
  });
}

// ── Logo ──────────────────────────────────────────────────────────────────────
function renderLogo(server, logo) {
  const area = document.getElementById('logo-area');
  const name = server.name || 'hs';
  if (logo && logo.image) {
    const img = document.createElement('img');
    img.src = logo.image; img.alt = name; img.className = 'logo-img';
    img.style.height = (logo.height||48)+'px';
    img.onerror = () => { area.innerHTML = textLogo(name); };
    area.appendChild(img);
  } else {
    area.innerHTML = textLogo(name);
  }
}
function textLogo(name) {
  return `<div class="logo">${name.slice(0,-1)}<span class="logo-accent">${name.slice(-1)}</span></div>`;
}

// ── Info tables ───────────────────────────────────────────────────────────────
function renderInfoTables(cfg) {
  const s = cfg.server, h = cfg.hardware;
  const arch = (h && h.arch) ? h.arch : (_arch || null);

  const netRows = [
    ['hostname', s.fqdn],
    ['ip', `<span style="color:var(--ctp-sky)">${s.ip}</span>`],
    s.dns ? ['dns', `<span style="color:var(--ctp-green)">${s.dns}</span>`] : null,
    ['os', h.os], ['kernel', h.kernel],
  ].filter(Boolean);

  const hwRows = [
    ['board', h.board], ['cpu', h.cpu],
    ['arch', `<span id="info-arch-value" class="tag" style="background:rgba(198,160,246,0.15);color:var(--accent);border:1px solid rgba(198,160,246,0.3)">${arch||'detecting...'}</span>`],
    ['ram', h.ram], ['storage', h.storage],
  ].filter(Boolean);

  const build = rows => rows.map(([k,v])=>`<tr><td>${k}</td><td>${v}</td></tr>`).join('');
  document.getElementById('info-network').innerHTML = build(netRows);
  document.getElementById('info-hardware').innerHTML = build(hwRows);

  if (arch) {
    const badge = document.getElementById('arch-tag');
    if (badge) { badge.textContent = arch; badge.style.display = 'inline-block'; }
  }
}

// ── Boot ──────────────────────────────────────────────────────────────────────
async function boot() {
  const [cfg, svcData] = await Promise.all([fetchJSON('/config.json'), fetchJSON('/services.json')]);

  if (!cfg || !svcData) {
    document.body.innerHTML = `<div style="padding:2rem;font-family:monospace;color:#ed8796">
      Error: could not load config.json or services.json<br>
      Check that files are mounted at /config/</div>`;
    return;
  }

  // Scale
  document.documentElement.style.setProperty('--scale', cfg.display.scale || 1.0);

  // Theme + clock
  initTheme(cfg.display.theme);
  startClock(cfg.display.timezone);

  // Title + logo
  document.title = cfg.server.fqdn + ' — Dashboard';
  renderLogo(cfg.server, cfg.logo);
  document.getElementById('logo-sub').textContent = cfg.server.description || cfg.server.fqdn;
  document.getElementById('footer-fqdn').textContent = cfg.server.fqdn;

  // System info
  renderInfoTables(cfg);

  // Services
  const initStatus = await fetchJSON('/api/status');
  statusData = (initStatus && initStatus.services) ? initStatus.services : {};
  renderServices(svcData.servers);

  // Stats
  const initStats = await fetchJSON('/api/stats');
  updateStats(initStats);

  // Refresh intervals
  const sm = (cfg.display.refresh_stats_ms  || 5000);
  const stm = (cfg.display.refresh_status_ms || 30000);
  setInterval(async () => updateStats(await fetchJSON('/api/stats')), sm);
  setInterval(refreshStatus, stm);
}

boot();
